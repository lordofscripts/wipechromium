func Test_WrapError(t *testing.T) {
	wrapped := fmt.Errorf("Bad Thing Happened: %w", cmn.ErrNotBrowserCache)

	if errors.Unwrap(wrapped) != cmn.ErrNotBrowserCache {
		t.Errorf("Error wrapping not working.\n\t%s", wrapped)
	}
}

